<?php

namespace App\Repository;

use App\Entity\Article;
use Doctrine\Bundle\DoctrineBundle\Repository\ServiceEntityRepository;
use Doctrine\ORM\OptimisticLockException;
use Doctrine\ORM\ORMException;
use Doctrine\Persistence\ManagerRegistry;

/**
 * @method Article|null find($id, $lockMode = null, $lockVersion = null)
 * @method Article|null findOneBy(array $criteria, array $orderBy = null)
 * @method Article[]    findAll()
 * @method Article[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class ArticleRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Article::class);
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function add(Article $entity, bool $flush = true): void
    {
        $this->_em->persist($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function remove(Article $entity, bool $flush = true): void
    {
        $this->_em->remove($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    /**
     * find by multiple criteria
     * @param $value
     * @return Article|null
     * @throws \Doctrine\ORM\NonUniqueResultException
     */
    public function findByMultipleCriteria($value)
    {
        return $this->createQueryBuilder('a')
            ->where('a.title = :val1')
            ->setParameter('val1', $value)
            ->orWhere('a.content like :val2')
            ->setParameter('val2', '%'.$value.'%')
            ->orWhere('a.introduction like :val')
            ->setParameter('val', '%'.$value.'%')
            ->getQuery()
            ->getOneOrNullResult() //to return one or null result
            ;
    }

